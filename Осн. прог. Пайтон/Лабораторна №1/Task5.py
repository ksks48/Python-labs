import math
print("Площа будь-якого трикутника за формулою Герона:")
print("----------------------------------------------------------")

a = float(input("Введіть довжину 1-ї сторони трикутника: "))
b = float(input("Введіть довжину 2-ї сторони трикутника: "))
c = float(input("Введіть довжину 3-ї сторони трикутника: "))

s = (a + b + c)/2
Area = math.sqrt(s * (s - a) * (s - b) * (s - c))

print("Площа трикутника дорівнює: " + str(Area)[:7])
