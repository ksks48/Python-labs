import math
print("Програма для розрахунку кількості фарби для циліндричного бака")
print ("------------------------------------------------------")
d = float(input("Діаметр основи d="))
h = float(input("Висота h="))
c = float(input("Витрата фарби c="))

s = 2*math.pi*d/2*(h+d/2)
print("Площа поверхні резервуара становить " + str(s)[:6] + " кв.м.")
L = s/c
print("Всього потрібно " + str(L)[:5] + " літрів фарби")

