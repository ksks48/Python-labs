x = float(input("Введіть першу координату точки(x): ") )
y = float(input("Введіть другу координату точки(y): ") )

if -1<=x<=1 and -1<=y<=1:
    if  y>x-1 and (y>-x-1) and (y<x+1) and (y<-x+1):
        print ("Точка не входить у задану область!")
    else:
        print ("Точка входить у задану область!")
else:
    print ("Точка не входить у задану область!")
